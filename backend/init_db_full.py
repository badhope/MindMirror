def init_db(db: Session):
    """初始化数据库数据"""
    # 清除旧数据
    print("正在清除旧数据...")
    db.query(Option).delete()
    db.query(Question).delete()
    db.query(Assessment).delete()
    db.query(User).delete()
    db.commit()

    print("开始初始化数据库...")

    # 创建初始用户
    demo_user = User(
        id=str(uuid.uuid4()),
        email="demo@example.com",
        hashed_password=get_password_hash("demo123"),
        name="演示用户"
    )
    db.add(demo_user)
    db.flush()
    print("✅ 创建演示用户")

    # 创建大五人格测评
    big5_id = str(uuid.uuid4())
    big5 = Assessment(
        id=big5_id,
        title="完整大五人格测评",
        description="全面评估你的开放性、尽责性、外向性、宜人性和神经质，共120题，帮助你深入了解自己的性格特征",
        category="性格",
        version="2.0",
        total_questions=120,
        is_active=True
    )
    db.add(big5)
    db.flush()

    # 创建120个问题
    for idx, q_data in enumerate(questions_data):
        q = Question(
            id=str(uuid.uuid4()),
            assessment_id=big5_id,
            question_text=q_data["text"],
            question_type="single",
            sort_order=idx+1
        )
        db.add(q)
        db.flush()
        
        # 创建选项
        options = []
        for opt_idx, (opt_text, score, dim) in enumerate(q_data["options"]):
            option = Option(
                id=str(uuid.uuid4()),
                question_id=q.id,
                option_text=opt_text,
                score_value=score,
                dimension=dim,
                sort_order=opt_idx+1
            )
            options.append(option)
        db.add_all(options)
        
        if (idx + 1) % 20 == 0:
            print(f"✅ 已创建 {idx + 1}/{len(questions_data)} 道题目")

