export function calculateKolbScores(answers: Array<{ questionId: string; value?: number }>) {
  const dimensions = {
    concreteExperience: 0,
    reflectiveObservation: 0,
    abstractConceptualization: 0,
    activeExperimentation: 0,
  }
  const counts = { ...dimensions }
  
  const dimensionMap: Record<string, keyof typeof dimensions> = {
    'kolb-001': 'concreteExperience',
    'kolb-002': 'reflectiveObservation',
    'kolb-003': 'abstractConceptualization',
    'kolb-004': 'activeExperimentation',
    'kolb-005': 'concreteExperience',
    'kolb-006': 'reflectiveObservation',
    'kolb-007': 'abstractConceptualization',
    'kolb-008': 'activeExperimentation',
    'kolb-009': 'concreteExperience',
    'kolb-010': 'reflectiveObservation',
    'kolb-011': 'abstractConceptualization',
    'kolb-012': 'activeExperimentation',
  }
  
  answers.forEach(a => {
    const dim = dimensionMap[a.questionId]
    if (dim && a.value !== undefined && a.value > 0) {
      dimensions[dim] += a.value
      counts[dim]++
    }
  })
  
  const scores = Object.fromEntries(
    Object.entries(dimensions).map(([k, v]) => [k, counts[k] > 0 ? Math.round((v / (counts[k] * 5)) * 100) : 50])
  ) as Record<string, number>
  
  const ac = scores.activeExperimentation - scores.reflectiveObservation
  const cc = scores.abstractConceptualization - scores.concreteExperience
  
  let style: keyof typeof KOLB_STYLES
  if (ac < 0 && cc < 0) style = 'diverging'
  else if (ac < 0 && cc >= 0) style = 'assimilating'
  else if (ac >= 0 && cc >= 0) style = 'converging'
  else style = 'accommodating'
  
  return { scores, style, processing: ac, perception: cc }
}

export function calculateKolbProfessional(answers: Array<{ questionId: string; value?: number }>): ProfessionalAssessmentResult {
  const result = calculateKolbScores(answers)
  const styleName = KOLB_STYLES[result.style]
  const styleDescriptions: Record<string, string> = {
    diverging: '擅长从多角度观察，善于团队合作，富有想象力和创造力',
    assimilating: '擅长归纳总结，构建理论模型，偏好逻辑清晰的信息',
    converging: '擅长解决实际问题，偏好技术应用和实践验证',
    accommodating: '擅长动手执行，勇于尝试新事物，适应变化能力强',
  }
  
  return generateProfessionalResult({
    type: 'kolb-advanced',
    title: 'Kolb学习风格专业报告',
    description: `你的学习风格为：${styleName}`,
    score: 75,
    accuracy: 85,
    dimensions: [
      { name: KOLB_DIMENSIONS.concreteExperience, score: result.scores.concreteExperience, maxScore: 100, description: '体验式学习偏好' },
      { name: KOLB_DIMENSIONS.reflectiveObservation, score: result.scores.reflectiveObservation, maxScore: 100, description: '反思深度' },
      { name: KOLB_DIMENSIONS.abstractConceptualization, score: result.scores.abstractConceptualization, maxScore: 100, description: '理论思维能力' },
      { name: KOLB_DIMENSIONS.activeExperimentation, score: result.scores.activeExperimentation, maxScore: 100, description: '实践行动力' },
    ],
    strengths: [styleDescriptions[result.style]],
    weaknesses: [],
    careers: [],
  }, 'professional')
}

